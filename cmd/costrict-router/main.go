func main() {
	// 统一解析子命令并把错误收口到同一种用户可见输出。
	if len(os.Args) < 2 {
		usage()
		os.Exit(2)
	}
	var err error
	switch os.Args[1] {
	case "login":
		err = cmdLogin(os.Args[2:])
	case "serve":
		err = cmdServe(os.Args[2:])
	case "models":
		err = cmdModels(os.Args[2:])
	case "start":
		err = cmdStart(os.Args[2:])
	case "stop":
		err = cmdStop(os.Args[2:])
	case "status":
		err = cmdStatus(os.Args[2:])
	case "restart":
		err = cmdRestart(os.Args[2:])
	case "logs":
		err = cmdLogs(os.Args[2:])
	case "key":
		err = cmdKey(os.Args[2:])
	default:
		usage()
		os.Exit(2)
	}
	if err != nil {
		fmt.Fprintln(os.Stderr, i18n.T("Error:", "错误:"), err)
		os.Exit(1)
	}
}

