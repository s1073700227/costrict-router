func readDaemonState(path string) (daemonState, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return daemonState{}, fmt.Errorf(i18n.T("failed to read PID file: %w", "读取 PID 文件失败: %w"), err)
	}
	var state daemonState
	if err := json.Unmarshal(data, &state); err != nil {
		return daemonState{}, err
	}
	if state.PID <= 0 || state.Addr == "" {
		return daemonState{}, fmt.Errorf(i18n.T("invalid PID file content: %s", "PID 文件内容无效: %s"), path)
	}
	return state, nil
}

func writeDaemonState(path string, state daemonState) error {
	if err := os.MkdirAll(filepath.Dir(path), 0o700); err != nil {
		return err
	}
	data, err := json.MarshalIndent(state, "", "  ")
	if err != nil {
		return err
	}
	return os.WriteFile(path, data, 0o600)
}

func waitForHealth(addr string, timeout time.Duration) error {
	deadline := time.Now().Add(timeout)
	var lastErr error
	for time.Now().Before(deadline) {
		if _, err := fetchHealth(addr); err == nil {
			return nil
		} else {
			lastErr = err
		}
		time.Sleep(200 * time.Millisecond)
	}
	if lastErr != nil {
		return lastErr
	}
	return fmt.Errorf(i18n.T("health check timed out", "健康检查超时"))
}

func fetchHealth(addr string) (string, error) {
	resp, err := http.Get("http://" + addr + "/healthz")
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return "", err
	}
	if resp.StatusCode < 200 || resp.StatusCode >= 300 {
		return "", fmt.Errorf("HTTP %d %s", resp.StatusCode, string(body))
	}
	var pretty bytes.Buffer
	if json.Indent(&pretty, body, "", "  ") == nil {
		return pretty.String(), nil
	}
	return string(body), nil
}

func tailLinesWithOffset(path string, n int) (int64, string, error) {
	if n <= 0 {
		n = 1000
	}
	data, err := os.ReadFile(path)
	if err != nil {
		return 0, "", err
	}
	text := string(data)
	lines := strings.SplitAfter(text, "\n")
	if len(lines) > n {
		lines = lines[len(lines)-n:]
	}
	return int64(len(data)), strings.Join(lines, ""), nil
}

func followLog(path string, offset int64, plain bool) error {
	// 用轻量轮询实现跨平台 tail -f，同时处理日志轮转导致的文件截断。
	ctx, stop := signal.NotifyContext(context.Background(), os.Interrupt, syscall.SIGTERM)
	defer stop()
	ticker := time.NewTicker(500 * time.Millisecond)
	defer ticker.Stop()
	for {
		select {
		case <-ctx.Done():
			return nil
		case <-ticker.C:
			info, err := os.Stat(path)
			if err != nil {
				return err
			}
			if info.Size() < offset {
				offset = 0
			}
			if info.Size() == offset {
				continue
			}
			file, err := os.Open(path)
			if err != nil {
				return err
			}
			if _, err := file.Seek(offset, io.SeekStart); err != nil {
				_ = file.Close()
				return err
			}
			chunk, err := io.ReadAll(file)
			_ = file.Close()
			if err != nil {
				return err
			}
			offset = info.Size()
			text := string(chunk)
			if plain {
				text = logx.StripANSI(text)
			}
			fmt.Print(text)
		}
	}
}

